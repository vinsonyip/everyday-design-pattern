package filter;

public class User {
	String name;
	String gender;
	int age;
	
	public User() {
		super();
	}
	
	public User(String name, String gender, int age) {
		super();
		this.name = name;
		this.gender = gender;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public String getGender() {
		return gender;
	}

	public int getAge() {
		return age;
	}
	
	
	
}
